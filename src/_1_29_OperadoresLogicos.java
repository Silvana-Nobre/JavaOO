

/**
Operadores Logicos

Operador	Operacao Logica
==			igual
!=			diferente
>			maior
>=			maior igual
<			menor
<=			menor igual
			
&&			e
||			ou

*/
public class _1_29_OperadoresLogicos {

	public static void main(String[] args) {
		int a = 1;
		
		if(a == 1) {
			System.out.println("a == 1");
		}
		
		if(a != 1) {
			System.out.println("a != 1");
		}
		
		if(a != 2) {
			System.out.println("a != 2");
		}
		
		if(a > 1) {
			System.out.println("a > 1");
		}
		
		if(a >= 1) {
			System.out.println("a >= 1");
		}

		if(a < 1) {
			System.out.println("a < 1");
		}
		
		if(a <= 1) {
			System.out.println("a <= 1");
		}
	}
}
