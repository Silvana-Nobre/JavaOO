

public class _1_33_SwitchDefault {

	public static void main(String[] args) {
		int a = 2;
		
		
		switch (a) {
			case 6: System.out.println("a == 6");
			break;
			case 7: System.out.println("a == 7");
			break;
			case 8: System.out.println("a == 8");
			break;
			default: System.out.println("se nao for nenhum vai esse!!!");
		}
	}
}
