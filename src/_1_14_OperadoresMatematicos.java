

/**
+	soma
-	subtraçao
*	multiplicaçao
/	divisao
%	resto de divisao
++	incremento
--	decremento
*/
public class _1_14_OperadoresMatematicos {

	public static void main(String[] args) {
		int a = 1;
		int b = 10;
		
		System.out.println((a+b));
		
		int c = a + b;
		
		System.out.println(c);
		
		c = c % 2;
		
		System.out.println(c);
		
		c++;
		System.out.println(c);
		
		a--;
		System.out.println(a);
		
		b += 3;
		System.out.println(a);
	}
}
